package sample.basics;

public class DeclareAndInitalizeVariables {

	public static void main(String[] args) {
		int val = 20;
		char a1 ='c';
		float f = 12.2f;
		int d = (int) f;
		System.out.println("val :"+val);
		System.out.println("char :"+a1);
		System.out.println("float :"+f);
		System.out.println("int :"+ d);
		
	
	}
}
